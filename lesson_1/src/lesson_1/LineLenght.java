package lesson_1;

public class LineLenght {

	public static void main(String[] args) {
		String s = ("Я стану отличным программистом!");
		int lenS = s.length();
		System.out.println( "Длина строки: " + lenS + " символ.");
		
		String cat = "Кот";
		int lenCat = cat.length();
		System.out.println( "Длина кота: " + lenCat + " символ.");
		
		String dog = "Собака";
		int lenDog = dog.length();
		System.out.println( "Длина собаки: " + lenDog + " символ.");
	}

}
